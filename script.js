// History Functions
function saveHistory(taskName, isChecked, comment = "") {
    const date = new Date();
    const fecha = date.toLocaleDateString();
    const hora = date.toLocaleTimeString();
    
    const existing = bboy_history[taskName];
    let finalComment = comment;
    if (!comment && existing && existing.fecha === fecha) {
        finalComment = existing.comentario || "";
    }

    bboy_history[taskName] = {
        fecha: fecha,
        hora: hora,
        dia: currentDayKey,
        fase: currentPhaseTitle,
        ejercicio: taskName,
        completado: isChecked,
        comentario: finalComment
    };
    localStorage.setItem('bboy_history', JSON.stringify(bboy_history));
}

function exportToCSV() {
    const headers = ["Fecha", "Hora", "Dia", "Fase", "Ejercicio", "Estado", "Comentarios"];
    let csvContent = '"' + headers.join('","') + '"\n';

    for (const key in bboy_history) {
        const record = bboy_history[key];
        const cleanComment = record.comentario ? record.comentario.replace(/"/g, '""').replace(/\n/g, " ") : "";
        const cleanEjercicio = record.ejercicio ? record.ejercicio.replace(/"/g, '""') : "";
        
        const row = [
            `"${record.fecha}"`,
            `"${record.hora}"`,
            `"${record.dia}"`,
            `"${record.fase}"`,
            `"${cleanEjercicio}"`,
            `"${record.completado ? "Completado" : "Pendiente"}"`,
            `"${cleanComment}"`
        ];
        csvContent += row.join(",") + "\n";
    }

    const blob = new Blob(['\uFEFF' + csvContent], { type: 'text/csv;charset=utf-8;' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.setAttribute("href", url);
    link.setAttribute("download", "historial_entrenamiento.csv");
    link.style.visibility = 'hidden';
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
}


